"""
Explain the difference between the = operator and the == operator in Python.

= Operator (Assignment)
Purpose: The = operator is used for assignment. It assigns the value on the right side to the variable on the left side.

Usage: It sets the variable to hold a specific value.
"""
#Example
x=5          # Here, 5 is assigned to the variable x
y=6          # Here, 6 is assigned to the variable y.
y=x          # Now, y holds the same value as x, which is 5.
print(x)

"""
== Operator (Equality Comparison)
Purpose: The == operator is used for comparison. It checks whether the values on both sides are equal.

Usage: It returns True if the values are equal, and False if they are not.
"""
#example
x = 5
y = 10
print(x == y)  # This will print False because 5 is not equal to 10.

z = 5
print(x == z)  # This will print True because 5 is equal to 5.








